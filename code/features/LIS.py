	def lis(self, seq):
		length = len(seq)
	 
		lis = [1]*length
	 
		for i in range (1, length):
			for j in range(i):
				if seq[i] > seq[j] and lis[i] < lis[j]+1:
					lis[i] = lis[j]+1
	 
		maxx = 0
	 
		for i in range(length):
			maxx = max(maxx, lis[i])
	 
		return maxx

